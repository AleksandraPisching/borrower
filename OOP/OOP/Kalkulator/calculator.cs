namespace OOP
{
    internal class calculator
    {

        public void Add(int first, int second)
        {
            return first + second;
        }
        public void Odejmowanie(int first, int second)
        {
            return first - second;
        }
        public void Mnożenie(int first, int second)
        {
            return first * second;
        }
        public void Dzielenia(int first, int second)
        {
            return first / second;
        }
    }
}
